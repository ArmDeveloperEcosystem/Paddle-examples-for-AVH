#include <stdio.h>
#include <tvm_runtime.h>
#include <tvmgen_cls.h>

// Header files generated by convert_image.py
#include "inputs.h"
#include "outputs.h"


int main(int argc, char** argv) {
  UartStdOutInit();
  printf("Starting cls inference\n");
  struct tvmgen_cls_outputs cls_outputs = {
      .output = output,
  };
  struct tvmgen_cls_inputs cls_inputs = {
      .inputs = input,
  };

  tvmgen_cls_run(&cls_inputs, &cls_outputs);

  // post process
  int index = 0;
  for(int i = 0; i < output_len;i++){
    if(output[i] > output[index]){
        index = i;
    }
  }
  printf("%d,%f\n", index, output[index]);

  // The FVP will shut down when it clseives "EXITTHESIM" on the UART
  printf("EXITTHESIM\n");
  while (1 == 1)
    ;
  return 0;
}